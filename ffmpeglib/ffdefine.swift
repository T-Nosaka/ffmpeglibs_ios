/*
 * AVMediaType型
 */
public enum AVMediaType: Int {
    case unknown = -1
    case video = 0
    case audio = 1
    case data = 2
    case subtitle = 3
    case attachment = 4
    case nb = 5
    
    static func valueOf(_ typeId: Int) -> AVMediaType {
        return AVMediaType(rawValue: typeId) ?? .unknown
    }
}

